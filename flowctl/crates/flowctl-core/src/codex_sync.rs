#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_map_model_opus() {
        let m = map_model("opus", "worker");
        assert_eq!(m.codex_model, "gpt-5.4");
        assert_eq!(m.reasoning_effort.as_deref(), Some("high"));
    }

    #[test]
    fn test_map_model_sonnet_fast() {
        let m = map_model("sonnet", "worker");
        assert_eq!(m.codex_model, "gpt-5.4-mini");
        assert!(m.reasoning_effort.is_none());
    }

    #[test]
    fn test_map_model_sonnet_intelligent_scout() {
        let m = map_model("sonnet", "epic-scout");
        assert_eq!(m.codex_model, "gpt-5.4");
        assert_eq!(m.reasoning_effort.as_deref(), Some("high"));
    }

    #[test]
    fn test_map_model_haiku() {
        let m = map_model("haiku", "worker");
        assert_eq!(m.codex_model, "gpt-5.4-mini");
        assert!(m.reasoning_effort.is_none());
    }

    #[test]
    fn test_map_model_inherit() {
        let m = map_model("inherit", "worker");
        assert!(m.codex_model.is_empty());
        assert!(m.reasoning_effort.is_none());
    }

    #[test]
    fn test_map_model_empty() {
        let m = map_model("", "worker");
        assert!(m.codex_model.is_empty());
        assert!(m.reasoning_effort.is_none());
    }

    #[test]
    fn test_sandbox_for_worker() {
        assert_eq!(sandbox_for("worker"), "workspace-write");
        assert_eq!(sandbox_for("plan-sync"), "workspace-write");
    }

    #[test]
    fn test_sandbox_for_scout() {
        assert_eq!(sandbox_for("epic-scout"), "read-only");
        assert_eq!(sandbox_for("anything"), "read-only");
    }

    #[test]
    fn test_generate_agent_toml_basic() {
        let fm = AgentFrontmatter {
            name: "test-agent".to_string(),
            description: "A test agent".to_string(),
            model: "opus".to_string(),
            disallowed_tools: None,
            color: None,
        };
        let mapping = ModelMapping {
            codex_model: "gpt-5.4".to_string(),
            reasoning_effort: Some("high".to_string()),
        };
        let toml = generate_agent_toml(&fm, "Do stuff.\n", &mapping);
        assert!(toml.contains("name = \"test-agent\""));
        assert!(toml.contains("model = \"gpt-5.4\""));
        assert!(toml.contains("model_reasoning_effort = \"high\""));
        assert!(toml.contains("sandbox_mode = \"read-only\""));
        assert!(toml.contains("Do stuff."));
    }

    #[test]
    fn test_generate_agent_toml_no_reasoning() {
        let fm = AgentFrontmatter {
            name: "worker".to_string(),
            description: "Worker".to_string(),
            model: "sonnet".to_string(),
            disallowed_tools: None,
            color: None,
        };
        let mapping = ModelMapping {
            codex_model: "gpt-5.4-mini".to_string(),
            reasoning_effort: None,
        };
        let toml = generate_agent_toml(&fm, "body\n", &mapping);
        assert!(!toml.contains("model_reasoning_effort"));
        assert!(toml.contains("sandbox_mode = \"workspace-write\""));
    }

    #[test]
    fn test_generate_agent_toml_escapes_backslashes() {
        let fm = AgentFrontmatter {
            name: "esc".to_string(),
            description: "d".to_string(),
            model: "opus".to_string(),
            disallowed_tools: None,
            color: None,
        };
        let mapping = ModelMapping {
            codex_model: "gpt-5.4".to_string(),
            reasoning_effort: None,
        };
        let toml = generate_agent_toml(&fm, "path\\to\\thing\n", &mapping);
        assert!(toml.contains("path\\\\to\\\\thing"));
    }

    #[test]
    fn test_generate_plugin_json() {
        let json = generate_plugin_json("1.0.0", "flow-code", "A plugin");
        assert!(json.contains("flow-code"));
        assert!(json.contains("1.0.0"));
    }

    #[test]
    fn test_generate_marketplace_json() {
        let json = generate_marketplace_json("flow-code");
        assert!(json.contains("flow-code"));
        assert!(json.contains(".codex-plugin"));
    }

    #[test]
    fn test_generate_hooks_json_patches_bash() {
        let input = r#"{"hooks":[{"matcher":"Bash","command":"echo hi"}]}"#;
        let out = generate_hooks_json(input).unwrap();
        assert!(out.contains("Bash|Execute"));
        assert!(!out.contains("\"Bash\""));
    }

    #[test]
    fn test_generate_hooks_json_invalid() {
        let result = generate_hooks_json("not json");
        assert!(result.is_err());
    }

    #[test]
    fn test_parse_agent_valid() {
        let content = "---\nname: my-agent\ndescription: A test agent\nmodel: opus\ndisallowedTools: Edit, Write\ncolor: blue\n---\nBody instructions here.\n";
        let doc = frontmatter::parse::<AgentFrontmatter>(content).unwrap();
        assert_eq!(doc.frontmatter.name, "my-agent");
        assert_eq!(doc.frontmatter.description, "A test agent");
        assert_eq!(doc.frontmatter.model, "opus");
        assert_eq!(doc.frontmatter.disallowed_tools.as_deref(), Some("Edit, Write"));
        assert_eq!(doc.frontmatter.color.as_deref(), Some("blue"));
        assert!(doc.body.contains("Body instructions here."));
    }

    #[test]
    fn test_parse_agent_no_frontmatter() {
        let content = "No frontmatter at all, just plain text.";
        let result = frontmatter::parse::<AgentFrontmatter>(content);
        assert!(result.is_err());
    }

    #[test]
    fn test_parse_agent_invalid_yaml() {
        let content = "---\n: : : not valid yaml [[[{\n---\nBody.\n";
        let result = frontmatter::parse::<AgentFrontmatter>(content);
        assert!(result.is_err());
    }

    #[test]
    fn test_parse_agent_missing_model() {
        let content = "---\nname: incomplete\ndescription: Missing model field\n---\nBody.\n";
        let result = frontmatter::parse::<AgentFrontmatter>(content);
        assert!(result.is_err());
    }

    #[test]
    fn test_sync_all_dry_run() {
        let dir = tempfile::tempdir().unwrap();
        let agents = dir.path().join("agents");
        std::fs::create_dir(&agents).unwrap();

        // Write a valid agent md
        std::fs::write(
            agents.join("test-scout.md"),
            "---\nname: test-scout\ndescription: A scout\nmodel: sonnet\n---\nBody here.\n",
        )
        .unwrap();

        // Write an invalid file (should be skipped)
        std::fs::write(agents.join("bad.md"), "no frontmatter").unwrap();

        let out = dir.path().join("out");
        let summary = sync_all(&agents, None, &out, true).unwrap();
        assert_eq!(summary.agents_generated, 1);
        assert_eq!(summary.agents_skipped, 1);
        // Dry run: no output files
        assert!(!out.exists());
    }

    #[test]
    fn test_sync_all_writes_files() {
        let dir = tempfile::tempdir().unwrap();
        let agents = dir.path().join("agents");
        std::fs::create_dir(&agents).unwrap();

        std::fs::write(
            agents.join("worker.md"),
            "---\nname: worker\ndescription: Worker agent\nmodel: inherit\n---\nDo work.\n",
        )
        .unwrap();

        let out = dir.path().join("out");
        let summary = sync_all(&agents, None, &out, false).unwrap();
        assert_eq!(summary.agents_generated, 1);
        assert!(out.join("agents/worker.toml").exists());

        let content = std::fs::read_to_string(out.join("agents/worker.toml")).unwrap();
        assert!(content.contains("name = \"worker\""));
    }
}
