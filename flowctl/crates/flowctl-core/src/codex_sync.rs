/// Generate `.codex-plugin/plugin.json`.
pub fn generate_plugin_json(version: &str, name: &str, description: &str) -> String {
    serde_json::json!({
        "schema_version": "v1",
        "name": name,
        "description": description,
        "version": version
    })
    .to_string()
}

/// Generate `.agents/plugins/marketplace.json`.
pub fn generate_marketplace_json(plugin_name: &str) -> String {
    serde_json::json!({
        "plugins": [
            { "name": plugin_name, "path": ".codex-plugin" }
        ]
    })
    .to_string()
}

// ---------------------------------------------------------------------------
// Hooks patching
// ---------------------------------------------------------------------------

/// Patch a Claude Code `hooks.json` for Codex: replace `"Bash"` matchers
/// with `"Bash|Execute"`.
pub fn generate_hooks_json(source_hooks: &str) -> Result<String, CoreError> {
    let mut value: serde_json::Value = serde_json::from_str(source_hooks)
        .map_err(|e| CoreError::FrontmatterParse(format!("hooks JSON parse error: {e}")))?;

    patch_bash_matchers(&mut value);

    serde_json::to_string_pretty(&value)
        .map_err(|e| CoreError::FrontmatterParse(format!("hooks JSON serialize error: {e}")))
}

/// Recursively walk the JSON value and replace `"Bash"` strings with
/// `"Bash|Execute"` wherever they appear.
fn patch_bash_matchers(value: &mut serde_json::Value) {
    match value {
        serde_json::Value::String(s) if s == "Bash" => {
            *s = "Bash|Execute".to_string();
        }
        serde_json::Value::Array(arr) => {
            for item in arr.iter_mut() {
                patch_bash_matchers(item);
            }
        }
        serde_json::Value::Object(map) => {
            for v in map.values_mut() {
                patch_bash_matchers(v);
            }
        }
        _ => {}
    }
}

// ---------------------------------------------------------------------------
// sync_all
// ---------------------------------------------------------------------------

/// Synchronise all agent `.md` files into Codex `.toml` files.
///
/// If `dry_run` is true, no files are written but the summary still reflects
/// what *would* happen.
pub fn sync_all(
    agents_dir: &Path,
    hooks_path: Option<&Path>,
    output_dir: &Path,
    dry_run: bool,
) -> Result<SyncSummary, CoreError> {
    let mut summary = SyncSummary::default();

    let agents_out = output_dir.join("agents");
    if !dry_run {
        std::fs::create_dir_all(&agents_out).map_err(|e| {
            CoreError::FrontmatterParse(format!("cannot create output dir: {e}"))
        })?;
    }

    // Read all .md files from agents_dir
    let entries = std::fs::read_dir(agents_dir).map_err(|e| {
        CoreError::FrontmatterParse(format!("cannot read agents dir: {e}"))
    })?;

    for entry in entries {
        let entry = match entry {
            Ok(e) => e,
            Err(e) => {
                summary.errors.push(format!("readdir error: {e}"));
                summary.agents_skipped += 1;
                continue;
            }
        };

        let path = entry.path();
        if path.extension().and_then(|s| s.to_str()) != Some("md") {
            continue;
        }

        let content = match std::fs::read_to_string(&path) {
            Ok(c) => c,
            Err(e) => {
                summary
                    .errors
                    .push(format!("{}: read error: {e}", path.display()));
                summary.agents_skipped += 1;
                continue;
            }
        };

        let doc = match frontmatter::parse::<AgentFrontmatter>(&content) {
            Ok(d) => d,
            Err(e) => {
                summary
                    .errors
                    .push(format!("{}: parse error: {e}", path.display()));
                summary.agents_skipped += 1;
                continue;
            }
        };

        let mapping = map_model(&doc.frontmatter.model, &doc.frontmatter.name);
        let toml_content = generate_agent_toml(&doc.frontmatter, &doc.body, &mapping);

        if !dry_run {
            let out_path = agents_out.join(format!("{}.toml", doc.frontmatter.name));
            std::fs::write(&out_path, &toml_content).map_err(|e| {
                CoreError::FrontmatterParse(format!(
                    "cannot write {}: {e}",
                    out_path.display()
                ))
            })?;
        }

        summary.agents_generated += 1;
    }

    // Hooks
    if let Some(hp) = hooks_path {
        if hp.exists() {
            match std::fs::read_to_string(hp) {
                Ok(src) => match generate_hooks_json(&src) {
                    Ok(patched) => {
                        if !dry_run {
                            let hooks_out = output_dir.join("hooks.json");
                            std::fs::write(&hooks_out, &patched).map_err(|e| {
                                CoreError::FrontmatterParse(format!(
                                    "cannot write hooks: {e}"
                                ))
                            })?;
                        }
                        summary.hooks_generated = true;
                    }
                    Err(e) => {
                        summary.errors.push(format!("hooks patch error: {e}"));
                    }
                },
                Err(e) => {
                    summary.errors.push(format!("hooks read error: {e}"));
                }
            }
        }
    }

    Ok(summary)
}

