//! Codex sync: convert Claude Code agent `.md` files to OpenAI Codex `.toml` format.
//!
//! Used by `flowctl codex sync` to generate a `.codex-plugin/` directory from
//! the plugin's `agents/` Markdown definitions.

use std::path::Path;

use serde::Deserialize;

use crate::error::CoreError;
use crate::frontmatter;

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

/// Frontmatter fields expected in an agent `.md` file.
#[derive(Debug, Clone, Deserialize)]
pub struct AgentFrontmatter {
    pub name: String,
    pub description: String,
    pub model: String,
    #[serde(rename = "disallowedTools", default)]
    pub disallowed_tools: Option<String>,
    pub color: Option<String>,
}

/// Result of mapping a Claude model string to Codex equivalents.
#[derive(Debug, Clone)]
pub struct ModelMapping {
    pub codex_model: String,
    pub reasoning_effort: Option<String>,
}

/// Summary returned by [`sync_all`].
#[derive(Debug, Default)]
pub struct SyncSummary {
    pub agents_generated: usize,
    pub agents_skipped: usize,
    pub hooks_generated: bool,
    pub errors: Vec<String>,
}

// ---------------------------------------------------------------------------
// Intelligent-scout list
// ---------------------------------------------------------------------------

/// Scouts that should use the *intelligent* model even when their agent
/// definition says "sonnet".
const INTELLIGENT_SCOUTS: &[&str] = &["epic-scout", "claude-md-scout", "docs-gap-scout"];

// ---------------------------------------------------------------------------
// Model mapping
// ---------------------------------------------------------------------------

fn intelligent_model() -> String {
    std::env::var("CODEX_MODEL_INTELLIGENT").unwrap_or_else(|_| "gpt-5.4".to_string())
}

fn fast_model() -> String {
    std::env::var("CODEX_MODEL_FAST").unwrap_or_else(|_| "gpt-5.4-mini".to_string())
}

/// Map a Claude model identifier to a Codex model + optional reasoning effort.
pub fn map_model(claude_model: &str, agent_name: &str) -> ModelMapping {
    let lower = claude_model.to_lowercase();

    if lower == "opus" || lower.starts_with("claude-opus") {
        ModelMapping {
            codex_model: intelligent_model(),
            reasoning_effort: Some("high".to_string()),
        }
    } else if lower == "sonnet" || lower.starts_with("claude-sonnet") {
        if INTELLIGENT_SCOUTS.contains(&agent_name) {
            ModelMapping {
                codex_model: intelligent_model(),
                reasoning_effort: Some("high".to_string()),
            }
        } else {
            ModelMapping {
                codex_model: fast_model(),
                reasoning_effort: None,
            }
        }
    } else if lower == "haiku" || lower.starts_with("claude-haiku") {
        ModelMapping {
            codex_model: fast_model(),
            reasoning_effort: None,
        }
    } else if lower == "inherit" || lower.is_empty() {
        ModelMapping {
            codex_model: String::new(),
            reasoning_effort: None,
        }
    } else {
        ModelMapping {
            codex_model: intelligent_model(),
            reasoning_effort: Some("high".to_string()),
        }
    }
}

// ---------------------------------------------------------------------------
// Sandbox
// ---------------------------------------------------------------------------

/// Return the Codex sandbox mode for a given agent name.
pub fn sandbox_for(name: &str) -> &'static str {
    match name {
        "worker" | "plan-sync" => "workspace-write",
        _ => "read-only",
    }
}

// ---------------------------------------------------------------------------
// TOML generation
// ---------------------------------------------------------------------------

/// Generate the `.toml` content for a single agent.
///
/// Built manually (not via serde) because `developer_instructions` uses
/// TOML multi-line basic strings (`"""`).
pub fn generate_agent_toml(
    fm: &AgentFrontmatter,
    body: &str,
    mapping: &ModelMapping,
) -> String {
    let escaped_body = body.replace('\\', "\\\\");
    let sandbox = sandbox_for(&fm.name);

    let mut out = String::with_capacity(256 + escaped_body.len());
    out.push_str("# Auto-generated by flowctl codex sync — do not edit manually\n");
    out.push_str(&format!("name = {:?}\n", fm.name));
    out.push_str(&format!("description = {:?}\n", fm.description));

    if !mapping.codex_model.is_empty() {
        out.push_str(&format!("model = {:?}\n", mapping.codex_model));
    }
    if let Some(ref effort) = mapping.reasoning_effort {
        out.push_str(&format!("model_reasoning_effort = {:?}\n", effort));
    }

    out.push_str(&format!("sandbox_mode = {:?}\n", sandbox));
    out.push_str(&format!(
        "\ndeveloper_instructions = \"\"\"\n{}\"\"\"\n",
        escaped_body
    ));
    out
}

// ---------------------------------------------------------------------------
// Plugin / marketplace JSON
